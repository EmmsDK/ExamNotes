package Loops;

public class Main {

    public static void main(String[] args) {
        int i = 0;

        // While loops

        while (i < 5) {
            System.out.println(i);

            i++;
        }

        // For Loops

        for (int j = 0; j < 5; j++) {
            System.out.println(j);
        }
    }
}
